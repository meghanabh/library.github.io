	<h1>Brief about the book</h1>
	<p>Pride and Prejudice is set in rural England in the early 19th century, and it follows the Bennet family, which includes five very different sisters. Mrs. Bennet is anxious to see all her daughters married, especially as the modest family estate is to be inherited by William Collins when Mr. Bennet dies. At a ball, the wealthy and newly arrived Charles Bingley takes an immediate interest in the eldest Bennet daughter, the beautiful and shy Jane. The encounter between his friend Darcy and Elizabeth is less cordial. Although Austen shows them intrigued by each other, she reverses the convention of first impressions: pride of rank and fortune and prejudice against the social inferiority of Elizabeth’s family hold Darcy aloof, while Elizabeth is equally fired both by the pride of self-respect and by prejudice against Darcy’s snobbery.
	The pompous Collins subsequently arrives, hoping to marry one of the Bennet sisters. Elizabeth, however, refuses his offer of marriage, and he instead becomes engaged to her friend Charlotte Lucas. During this time, Elizabeth encounters the charming George Wickham, a military officer. There is a mutual attraction between the two, and he informs her that Darcy has denied him his inheritance.
	He also discloses that Wickham, after squandering his inheritance, tried to marry Darcy’s then 15-year-old sister in an attempt to gain possession of her fortune. With these revelations, Elizabeth begins to see Darcy in a new light.</p>

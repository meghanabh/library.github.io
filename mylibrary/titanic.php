	<h1>Brief about the book</h1>
	<p>The film opens with images of the Titanic’s departure from Southampton in April, 1912. In the present day, treasure hunter Brock Lovett leads a team of submersibles down into the Titanic’s wreck. He finds a safe containing a drawing of a nude woman wearing a necklace he is seeking, called “the Heart of the Ocean.” Brock receives a phone call from a 101-year old woman claiming to be the subject of the drawing, and he flies her out to his research vessel to hear her story.

Named Rose Dewitt Bukater, she explains to Brock and his team that she had boarded the Titanic in Southampton with her fiancé, Cal Hockley, and her mother Ruth. Thus begins the flashback which will be most of the film's narrative. We see Jack Dawson, the penniless artist with whom she will soon fall in love, winning tickets for the Titanic's voyage in a lucky round of poker in a nearby pub, and he boards the ship at the last minute. Rose describes the Titanic as “slave ship,” given how suffocated and unhappy she feels as Cal’s wife-to-be. After the ship departs from the harbor, Jack and his friend Fabrizio ecstatically rejoice at the ship’s bow. Rose dines in first class with other members of the upper crust, including Molly Brown, the shipbuilder Thomas Andrews, and White Star Line executive J. Bruce Ismay. Rose especially resents her mother and Cal’s controlling natures, and Ismay’s arrogance when describing the Titanic.

That night, Rose is about to commit suicide by hurling herself from the ship’s stern, when Jack happens upon her and convinces her to step back over the railing by saying he will jump in after her. White Star Line officials initially think Jack has attacked her, but Rose improvises a lie to exonerate Jack and conceal the motives behind her own behavior. Rose convinces Cal to invite Jack to dinner the following night. The next day, Rose strolls the deck with Jack, thanking him for his discretion. Initially shocked by his bluntness, Rose warms to Jack, especially impressed by his drawings.</p>

	<h1>Brief about the book</h1>
	<p>The History of Tom Jones, a Foundling is a 1749 novel by . It is his most famous work and was considered one of the greatest English novels ever written by subsequent authors like W. Somerset Maughan and Samuel Coleridge. It is divided into eighteen books and aims to explore human nature, covering a wide variety of social themes and literary styles.

The book opens by explaining Tom Jones’s origins. Squire Allworthy is a kind and noble man who one day comes home to Paradise Hall to find a baby on his bed, abandoned by his parents. He entrusts the child with his sister Bridget while they try to find out where the baby came from. Village gossip points to a woman called Jenny, so Allworthy brings her in for questioning.

Jenny admits to being the child’s mother and to abandoning him, but refuses to say who the father is. Allworthy gives her money to leave the area and start a new life and decides to bring the boy up himself, with Bridget’s help. Allworthy gives him the name Tom Jones. The townspeople assume that he must be the father because of his kindness.</p>
